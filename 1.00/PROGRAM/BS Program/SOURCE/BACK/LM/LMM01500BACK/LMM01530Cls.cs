        public List<LMM01531DTO> GetAllOtherChargesLookup(LMM01531DTO poEntity)
        {
            var loEx = new R_Exception();
            List<LMM01531DTO> loResult = null;

            try
            {
                var loDb = new R_Db();
                var loConn = loDb.GetConnection("R_DefaultConnectionString");
                var loCmd = loDb.GetCommand();

                var lcQuery = $"SELECT " +
                    $"'Unit Charge' AS UNIT_UTILITY_CHARGE, A.CCHARGES_TYPE, B.CDESCRIPTION AS CCHARGES_TYPE_DESCR, " +
                    $"A.CCHARGES_ID, A.CCHARGES_NAME " +
                    $"FROM LMM_UNIT_CHARGES A (NOLOCK) " +
                    $"INNER JOIN RFT_GET_GSB_CODE_INFO ('BIMASAKTI', @CCOMPANY_ID, '_BS_UNIT_CHARGES_TYPE', '', @CUSER_LANGUANGE ) " +
                    $"B ON A.CCHARGES_TYPE = B.CCODE " +
                    $"WHERE A.CCOMPANY_ID = @CCOMPANY_ID AND A.CPROPERTY_ID = @CPROPERTY_ID " +
                    $"AND A.CSTATUS = '80' " +
                    $"UNION ALL " +
                    $"SELECT " +
                    $"'Utility Charge' AS UNIT_UTILITY_CHARGE, A.CCHARGES_TYPE, B.CDESCRIPTION AS CCHARGES_TYPE_DESCR, " +
                    $"A.CCHARGES_ID, A.CCHARGES_NAME FROM LMM_UTILITY_CHARGES A (NOLOCK) " +
                    $"INNER JOIN RFT_GET_GSB_CODE_INFO ('BIMASAKTI', @CCOMPANY_ID, '_BS_UTILITY_CHARGES_TYPE', '', @CUSER_LANGUANGE ) B ON A.CCHARGES_TYPE = B.CCODE " +
                    $"WHERE A.CCOMPANY_ID = @CCOMPANY_ID AND A.CPROPERTY_ID = @CPROPERTY_ID " +
                    $"AND A.CSTATUS = '80'";
                loCmd.CommandText = lcQuery;

                loDb.R_AddCommandParameter(loCmd, "@CCOMPANY_ID", DbType.String, 50, poEntity.CCOMPANY_ID);
                loDb.R_AddCommandParameter(loCmd, "@CPROPERTY_ID", DbType.String, 50, poEntity.CPROPERTY_ID);
                loDb.R_AddCommandParameter(loCmd, "@CUSER_LANGUANGE", DbType.String, 50, poEntity.CUSER_LANGUANGE);

                var loDataTable = loDb.SqlExecQuery(loConn, loCmd, true);

                loResult = R_Utility.R_ConvertTo<LMM01531DTO>(loDataTable).ToList();

            }
            catch (Exception ex)
            {
                loEx.Add(ex);
            }

            loEx.ThrowExceptionIfErrors();

            return loResult;
        }

